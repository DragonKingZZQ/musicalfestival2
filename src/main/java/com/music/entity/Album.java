package com.music.entity;



public class Album {
	private Integer id;
	private String album_url;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getAlbum_url() {
		return album_url;
	}
	public void setAlbum_url(String album_url) {
		this.album_url = album_url;
	}
	
	
	
}
