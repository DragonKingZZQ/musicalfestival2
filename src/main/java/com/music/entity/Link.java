package com.music.entity;



public class Link {
	private int id;
	private int link_type;
	private String link_url;
	private String link_type_introduce;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getLink_type() {
		return link_type;
	}
	public void setLink_type(int link_type) {
		this.link_type = link_type;
	}
	public String getLink_url() {
		return link_url;
	}
	public void setLink_url(String link_url) {
		this.link_url = link_url;
	}
	public String getLink_type_introduce() {
		return link_type_introduce;
	}
	public void setLink_type_introduce(String link_type_introduce) {
		this.link_type_introduce = link_type_introduce;
	}
	
	
	
}
